    int XetSoLanBan(){
        int max = 1;
        ArrayList<BanHangCode> list = new ConnectionMySQL_SanPhamDaBan().getListBanHang();
        for(int i=0;i<list.size();i++){
            BanHangCode banHang = list.get(i) ;
            if(max < banHang.getLanBan())
                max = banHang.getLanBan();
        }
        return max;
    }

