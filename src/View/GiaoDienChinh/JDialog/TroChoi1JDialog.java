    void ChuyenIcon(java.awt.event.ActionEvent evt,int i,JButton x){
        if(evt.getActionCommand().equals(i+"")){
            try {
                x.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Image/BanHang/boom.png")));
                Thread.sleep(300); 
                SuKienTroChoi();
            } catch (Exception e) {} 
        }
        
        
    }
    public static int key  ;
    void SuKienTroChoi(){
        key = RanDom();
        if(key == 0){
            JOptionPane.showMessageDialog(this, "Chúc quý khách may mắn lần sau");
        }
        else if(key == 50){
            JOptionPane.showMessageDialog(this, "Quý khách đã nhận được giải thưởng cao nhất \nMột thẻ giảm giá 50%");
        }
        else{
            JOptionPane.showMessageDialog(this, "Chúc mừng quý khách đã nhận được mã giảm giá "+key+"%");
        } 
        try {
            Thread.sleep(500); 
            setVisible(false);
            PhuongThucThanhToanJDiolog hd = new PhuongThucThanhToanJDiolog(new GiaoDienChinh(),false);
            hd.setVisible(true);
        } catch (Exception e) {} 
    }
